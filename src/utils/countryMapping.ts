export const getCountryCode = (countryName: string): string => {
  // First try the main mapping
  const code = countryToCode[countryName];
  if (code) return code;

  // Then try variations
  const variationCode = countryVariations[countryName];
  if (variationCode) return variationCode;

  // If no mapping found, return the original name and log a warning in development
  if (process.env.NODE_ENV === 'development') {
    console.warn(`No country code mapping found for: ${countryName}`);
  }
  return countryName;
};
